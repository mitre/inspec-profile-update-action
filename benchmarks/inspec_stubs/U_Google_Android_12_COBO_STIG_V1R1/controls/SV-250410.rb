  title 'Google Android 12 must be configured to disallow configuration of date and time.'
  desc 'Determining the correct time a particular application event occurred on a system is critical when conducting forensic analysis and investigating system events. 
 
Periodically synchronizing internal clocks with an authoritative time source is necessary to correctly correlate the timing of events that occur across the enterprise. The three authoritative time sources for Google Android 12 are an authoritative time server that is synchronized with redundant United States Naval Observatory (USNO) time servers as designated for the appropriate DoD network (NIPRNet or SIPRNet), or the Global Positioning System (GPS), or the wireless carrier. 
 
Time stamps generated by the audit system in Google Android 12 must include both date and time. The time may be expressed in Coordinated Universal Time (UTC), a modern continuation of Greenwich Mean Time (GMT), or local time with an offset from UTC.

SFR ID: FMT_SMF_EXT.1.1 #47'
  desc 'check', 'Review the managed Google Android 12 device configuration settings to confirm that autofill services are disabled. 
 
This procedure is performed on both the EMM Administration console and the managed Google Android 12 device. 
 
On the EMM console:

COBO:

1. Open "Set user restrictions".
2. Verify that "Disallow config date time" is toggled to ON.

COPE:

1. Open "Set user restrictions on parent".
2. Verify that "Disallow config date time" is toggled to ON.
 
On the managed Google Android 12 device: 

COBO and COPE:

1. Open Settings. 
2. Tap "System".
3. Tap "Date & times". 
4. Verify that "Set time automatically" is grayed out and is "Enabled by admin".
 
If on the EMM console "Disallow config date time" is not set to On, or on the managed Android 12 device "User network-provided time" is not grayed out, this is a finding.'
  desc 'fix', 'Configure the Google Android 12 device to set auto network time.
 
On the EMM console:

COBO:

1. Open "Set user restrictions".
2. Toggle "Disallow config date time" to ON.

COPE:

1. Open "Set user restrictions on parent".
2. Toggle "Disallow config date time" to ON.'
