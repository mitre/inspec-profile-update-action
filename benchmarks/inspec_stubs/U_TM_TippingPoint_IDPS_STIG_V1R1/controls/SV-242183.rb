5. Select which device group it should be part of.
6. Ensure it is under the IPS/TPS device type. 
7. Click OK. 
8. Ensure the device connects and is managed by the SMS. 
9. On the serial console of the TPS type the command: show sms, and ensure it is now being controlled by SMS.'
