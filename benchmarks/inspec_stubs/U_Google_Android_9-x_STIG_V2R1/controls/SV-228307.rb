  desc 'Determining the correct time a particular application event occurred on a system is critical when conducting forensic analysis and investigating system events. 

Periodically synchronizing internal clocks with an authoritative time source is necessary to correctly correlate the timing of events that occur across the enterprise. The three authoritative time sources for Google Android Pie are an authoritative time server that is synchronized with redundant United States Naval Observatory (USNO) time servers as designated for the appropriate DoD network (NIPRNet or SIPRNet), or the Global Positioning System (GPS), or the wireless carrier. 

Time stamps generated by the audit system in Google Android Pie must include both date and time. The time may be expressed in Coordinated Universal Time (UTC), a modern continuation of Greenwich Mean Time (GMT), or local time with an offset from UTC.

SFR ID: FMT_SMF_EXT.1.1 #47'
  desc 'check', 'Review the Google Android Pie Work Profile configuration settings to confirm that autofill services are disabled. 

This procedure is performed on both the MDM Administration console and the Google Android Pie device. 

On the MDM console, verify that "Set auto (network) time required" to on".

On the Google Android Pie device, do the following: 
1. Open Settings. 
2. Tap "System". 
3. Tap "Date & times". 
4. Verify that "Automatic date & time" is grayed out.

If on the MDM console "Set auto (network) time required" to on", or on the Google Android Pie device "Automatic date & time" is grayed out, this is a finding.'
