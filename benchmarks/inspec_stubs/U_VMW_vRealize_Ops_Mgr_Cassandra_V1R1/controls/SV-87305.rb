  title 'The Cassandra Server must utilize centralized management of the content captured in audit records generated by all components of the system.'
  desc 'Without the ability to centrally manage the content captured in the audit records, identification, troubleshooting, and correlation of suspicious behavior would be difficult and could lead to a delayed or incomplete analysis of an ongoing attack.

The content captured in audit records must be managed from a central location (necessitating automation). Centralized management of audit records and logs provides for efficiency in maintenance and management of records, as well as the backup and archiving of those records. 

The DBMS may write audit records to database tables, to files in the file system, to other kinds of local repository, or directly to a centralized log management system. Whatever the method used, it must be compatible with off-loading the records to the centralized system.'
  desc 'check', 'Review the Cassandra Server settings to ensure centralized management of the content captured in audit records generated by all components of the system are utilized.

At the command prompt, execute the following command:

# grep SyslogAppender /usr/lib/vmware-vcops/user/conf/cassandra/logback.xml

If level is not set to "ALL", this is a finding.'
  desc 'fix', 'Configure the Cassandra Server to utilize centralized management of the content captured in audit records generated by all components of the system.

Navigate to and open /usr/lib/vmware-vcops/user/conf/cassandra/logback.xml.

Navigate to the <configuration> node.

Add the following <appender> node to the <configuration> node.

  <appender name="SYSLOG" class="ch.qos.logback.classic.net.SyslogAppender">
    <syslogHost>syslogServerHostName</syslogHost>
    <facility>AUTH</facility>
    <suffixPattern>%-5level [%thread] %date{ISO8601, UTC} %F:%L - %msg%n </suffixPattern>
  </appender>

Navigate to the <root> node.

Add the following to the <root> node.
    <appender-ref ref="SYSLOG" />'
