  title 'Microsoft Android 11 must be configured to disallow configuration of date and time.'
  desc 'Determining the correct time a particular application event occurred on a system is critical when conducting forensic analysis and investigating system events. 
 
Periodically synchronizing internal clocks with an authoritative time source is necessary to correctly correlate the timing of events that occur across the enterprise. The three authoritative time sources for Microsoft Android 11 are an authoritative time server that is synchronized with redundant United States Naval Observatory (USNO) time servers as designated for the appropriate DOD network (NIPRNet or SIPRNet), or the Global Positioning System (GPS), or the wireless carrier. 
 
Time stamps generated by the audit system in Microsoft Android 11 must include both date and time. The time may be expressed in Coordinated Universal Time (UTC), a modern continuation of Greenwich Mean Time (GMT), or local time with an offset from UTC.

SFR ID: FMT_SMF_EXT.1.1 #47'
  desc 'check', 'Review the Microsoft Android 11 Work Profile configuration settings to confirm that autofill services are disabled. 
 
This procedure is performed on both the EMM Administration console and the Microsoft Android 11 device. 
 
On the EMM console:
1. Open "Set user restrictions on parent".
2. Verify that "Disallow config date time" is toggled to "On."
 
On the Microsoft Android 11 device: 
1. Open "Settings". 
2. Tap "System". 
3. Tap "Date & times". 
4. Validate that "Use network-provided time" is set to "On" and grayed out.
 
If on the EMM console "Disallow config date time" is not enabled, or on the Microsoft Android 11 device "Use network-provided time" is not on and grayed out, this is a finding.'
  desc 'fix', 'Configure Microsoft Android 11 work Profile to set auto network time.
 
On the EMM console:
1. Open "Set user restrictions on parent".
2. Toggle "Disallow config date time" is "On".'
