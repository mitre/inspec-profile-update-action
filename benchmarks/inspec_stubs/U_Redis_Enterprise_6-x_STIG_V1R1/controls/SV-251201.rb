  title 'Redis Enterprise DBMS must record time stamps, in audit records and application data, that can be mapped to Coordinated Universal Time (UTC, formerly GMT).'
  desc 'If time stamps are not consistently applied and there is no common time reference, it is difficult to perform forensic analysis.

Time stamps generated by the DBMS must include date and time. Time is commonly expressed in Coordinated Universal Time (UTC), a modern continuation of Greenwich Mean Time (GMT), or local time with an offset from UTC. 

Some DBMS products offer a data type called TIMESTAMP that is not a representation of date and time. Rather, it is a database state counter and does not correspond to calendar and clock time. This requirement does not refer to that meaning of TIMESTAMP.

Redis Enterprise application components allow the setting of a UTC timestamp associated with the logs of the database.'
  desc 'check', "To check the timestamp, perform the following steps:
1. Log in to the Redis Enterprise Control Plane.
2. Navigate to the settings tab.
3. Navigate to the general tab of the settings tab.
4. On screen find the Time zone setting.
5. Verify that the time zone is mapped to UTC.

If the time zone isn't mapped to UTC, this is a finding."
  desc 'fix', 'To resolve an issue with the timestamps, perform the following steps:
1. Log in to the Redis Enterprise Control Plane.
2. Navigate to the settings tab.
3. Navigate to the general tab of the settings tab.
4. On screen find the Time zone setting.
5. Ensure that the time zone is mapped to UTC.
6. Click "Save".'
