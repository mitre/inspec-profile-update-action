  desc 'Determining the correct time a particular application event occurred on a system is critical when conducting forensic analysis and investigating system events. 

Periodically synchronizing internal clocks with an authoritative time source is necessary to correctly correlate the timing of events that occur across the enterprise. The three authoritative time sources for Samsung Android are an authoritative time server that is synchronized with redundant United States Naval Observatory (USNO) time servers as designated for the appropriate DoD network (NIPRNet or SIPRNet), or the Global Positioning System (GPS), or the wireless carrier. 

Time stamps generated by the audit system in Samsung Android must include both date and time. The time may be expressed in Coordinated Universal Time (UTC), a modern continuation of Greenwich Mean Time (GMT), or local time with an offset from UTC.

SFR ID: FMT_SMF_EXT.1.1 #47'
  desc 'check', 'Review device configuration settings to confirm that configuration of the date and time is disallowed. 

This procedure is performed on both the MDM Administration console and the Samsung Android device. 

On the MDM console, for the device, in the "Android user restrictions" group, verify that "disallow config date time" is selected. 

On the Samsung Android device, do the following: 
1. Open Settings. 
2. Tap "General management". 
3. Tap "Date and time". 
4. Verify that "Automatic date and time" is on and the user cannot disable it. 

If on the MDM console "disallow config date time" is not selected, or on the Samsung Android device "Automatic date and time" is not set or the user can disable it, this is a finding.'
  desc 'fix', 'Configure Samsung Android to disallow configuration of the date and time. 

On the MDM console, for the device, in the "Android user restrictions" group, select "disallow config date time".'
